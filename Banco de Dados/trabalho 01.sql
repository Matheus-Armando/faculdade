CREATE TABLE PESSOA(
    CODIGO_PESSOA BIGINT,
    NOME VARCHAR (60),
    CPF VARCHAR (11),
    DATA_NASCIMENTO DATE,
    TELEFONE VARCHAR(15),
    TIPO_PESSOA SMALLINT,
    SEXO VARCHAR (1),
