main() {
  var numero01 = 50;
  var numero02 = 10;

  var adicao = numero01 + numero02;
  var substracao = numero01 - numero02;
  var multiplicacao = numero01 * numero02;
  var divisao = numero01 / numero02;
  var restoDivisao = numero01 % numero02;

  print("A soma de $numero01 + $numero02 é: $adicao");
  print("A subtração de $numero01 - $numero02 é: $substracao");
  print("A multiplicação de $numero01 * $numero02 é: $multiplicacao");
  print("A divisão de $numero01 / $numero02 é: $divisao");
  print("A resto da divisão de $numero01 % $numero02 é: $restoDivisao");
}
